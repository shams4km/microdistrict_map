// Инициализация карты
function init() {
    // Координаты центра карты (Фестивальный микрорайон, Краснодар)
    const defaultCenter = [45.059988, 38.957221];
    
    // Создание карты
    myMap = new ymaps.Map("map", {
        center: defaultCenter,
        zoom: 15,
        controls: ['zoomControl', 'fullscreenControl', 'typeSelector']
    }, {
        suppressMapOpenBlock: true
    });
    
    // Добавление маркеров
    addMarkers();
    
    // Добавление границы района
    addDistrictBoundary();
    
    console.log("Карта Яндекс загружена!");
}

// Функция добавления маркеров
function addMarkers() {
    places.forEach(place => {
        // Определение цвета иконки в зависимости от типа
        let preset = "islands#blueDotIcon";
        switch(place.color) {
            case "#2196F3": preset = "islands#blueEducationIcon"; break;
            case "#4CAF50": preset = "islands#greenParkIcon"; break;
            case "#FF9800": preset = "islands#orangeMoneyIcon"; break;
            case "#F44336": preset = "islands#redHealthIcon"; break;
            case "#9C27B0": preset = "islands#violetBookIcon"; break;
            case "#FFC107": preset = "islands#yellowSportIcon"; break;
            case "#E91E63": preset = "islands#pinkPharmacyIcon"; break;
            case "#3F51B5": preset = "islands#blueOfficeIcon"; break;
        }
        
        // Создание метки
        const placemark = new ymaps.Placemark(place.coords, {
            balloonContentHeader: `<strong style="color: ${place.color}; font-size: 1.2rem;">${place.name}</strong>`,
            balloonContentBody: `<p style="color: #555; margin-top: 8px; line-height: 1.5;">${place.desc}</p>`,
            hintContent: place.name
        }, {
            preset: preset,
            iconColor: place.color
        });
        
        // Добавление метки на карту
        myMap.geoObjects.add(placemark);
        
        allPlacemarks.push(placemark);
        placemarks.push(placemark);
    });
}

